package CAMs;
/**
 * Enumeration representing the availability of a camp, either to a specific school or to the entire NTU.
 * @see Camp
 * @see Staff
 * @version 2.0
 * @since 2023-11-19
 */
public enum CampAvailability {
	/**
     * Represents a camp available to a specific school.
     */
    SCHOOL, 
    /**
     * Represents a camp available to the entire NTU.
     */
    NTU
}

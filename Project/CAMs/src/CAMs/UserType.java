package CAMs;
/**
 * Enumeration representing different types of users in the CAMs system.
 * Users can be either STUDENT or STAFF.
 * @see User
 * @see Student
 * @see Staff
 * @version 2.0
 * @since 2023-11-19
 */
public enum UserType {
	/**
     * Represents a student user type.
     */
    STUDENT,

    /**
     * Represents a staff user type.
     */
    STAFF
    }

package CAMs;
/**
 * Each constant in this enumeration represents a specific school within NTU.
 * This enum is used to categorize users and camps based on their associated school.
 *
 * @see User
 * @see Camp
 * @version 2.0
 * @since 2023-11-19
 */
public enum Schools {
	/**
     * School of Computer Science and Engineering.
     */
    SCSE,

    /**
     * School of Art, Design, and Media.
     */
    ADM,

    /**
     * School of Electrical and Electronic Engineering.
     */
    EEE,

    /**
     * Nanyang Business School.
     */
    NBS,

    /**
     * School of Social Sciences.
     */
    SSS
}
